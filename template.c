void ClawArm(int threshold)
{
	if(abs(vexRT[Ch2Xmtr2]) > threshold)         // If the right joystick is greater than or less than the threshold:
	{
	motor[leftClawArm] = vexRT[Ch2Xmtr2];
	motor[rightClawArm] = vexRT[Ch2Xmtr2];
	}
	else                                    // If the right joystick is within the threshold:
	{
		motor[leftClawArm] = 0;
		motor[rightClawArm] = 0;                // Stop the right motor (cancel noise)
	}
}

