void mecanum_Drive(int threshold, int speed)
{
	//Create "deadzone" variables. Adjust threshold value to increase/decrease deadzone



	//Create "deadzone" for Y1/Ch3
	if(abs(vexRT[Ch3]) > threshold)
		Y1 = vexRT[Ch3];
	else
		Y1 = 0;
	//Create "deadzone" for X1/Ch4
	if(abs(vexRT[Ch4]) > threshold)
		X1 = vexRT[Ch4];
	else
		X1 = 0;
	//Create "deadzone" for X2/Ch1
	if(abs(vexRT[Ch1]) > threshold)
		X2 = vexRT[Ch1];
	else
		X2 = 0;

	//Remote Control Commands
	motor[frontRightWheel] = (Y1 - X2 - X1)/speed;
	motor[backRightWheel] =  (Y1 - X2 + X1)/speed;
	motor[frontLeftWheel] = (Y1 + X2 + X1)/speed;
	motor[backLeftWheel] =  (Y1 + X2 - X1)/speed;

}
void tank_Drive(int threshold, int speed)
{
	if(abs(vexRT[Ch3]) > threshold)         // If the left joystick is greater than or less than the threshold:
	{
		motor[frontLeftWheel]  = (vexRT[Ch3])/speed;   // Left Joystick Y value / 2.
		motor[backLeftWheel]  = (vexRT[Ch3])/speed;
	}
	else                                    // If the left joystick is within the threshold:
	{
		motor[frontLeftWheel]  = 0;
		motor[backLeftWheel]  = 0;// Stop the left motor (cancel noise)
	}

	if(abs(vexRT[Ch2]) > threshold)         // If the right joystick is greater than or less than the threshold:
	{
		motor[frontRightWheel] = (vexRT[Ch2])/speed;   // Right Joystick Y value / 2.
		motor[backRightWheel] = (vexRT[Ch2])/speed;
	}
	else                                    // If the right joystick is within the threshold:
	{
		motor[frontRightWheel] = 0;
		motor[backRightWheel] = 0;                // Stop the right motor (cancel noise)
	}
}

